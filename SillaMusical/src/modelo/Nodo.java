public class Nodo {
    private Silla silla;
    private Nodo siguiente;

    public Nodo(Silla silla) {
        this.silla = silla;
        this.siguiente = this;
    }

    public Silla getSilla() {
        return silla;
    }

    public void setSilla(Silla silla) {
        this.silla = silla;
    }

    public Nodo getSiguiente() {
        return siguiente;
    }

    public void setSiguiente(Nodo siguiente) {
        this.siguiente = siguiente;
    }
}
